//Concatenación y Template Strings

const nombre = 'Juan';
const email = 'correo@correo.com';

// Concatenación
console.log("Nombre Cliente: " + nombre + " Email: " + email);


// Template Strings - String Literals
console.log(`Nombre Cliente: ${nombre} Email: ${email}`);

/* Template String: Es la concatenacion pero mas efectiva en JS y la que mas se usa, el 1er ejemplo ya casi que no. Para crearla como vemos, se encierra todo dentro de comillas invertida (es apretando 2 veces la tecla que esta a lado de la P), seguido de la variable entre corchetes y el signo $. */